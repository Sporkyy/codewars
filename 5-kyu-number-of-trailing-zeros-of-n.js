// 5 kyu | Number of trailing zeros of N!
// https://www.codewars.com/kata/number-of-trailing-zeros-of-n

// =-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=

const zeros = n =>
  Math.trunc(
    (n - [...n.toString(5)].map(Number).reduce((acc, curr) => acc + curr)) / 4,
  );

// =-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=

import { strictEqual } from 'assert';

strictEqual(zeros(0), 0);
strictEqual(zeros(5), 1);
strictEqual(zeros(6), 1);
strictEqual(zeros(30), 7);

